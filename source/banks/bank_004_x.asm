Fightscene_Update::
    ; Set up the main VBlank function
    Set16_M wVBlank_HandlerFunc, Fightscene00_VBlank_Main
    ; Set up an update palette VBlank function if necessary
    Do_CallForeign Palette_DeterminePaletteVBlankFunc


MACRO Creature_Table_Row
    SHIFT ; Empty first parameter in CSV file
.\1:
    db \2 ; Type
    db \3 ; Energy
    db \4 ; UNK
    db \5 ; MaxEnergy
    db \6 ; Strength
    db \7 ; Skill
    db \8 ; Speed
    db \9 ; Defense
    db \<10> ; Resist
    db \<11> ; Luck
    db \<12> ; UNKEnergyUp
    db \<13> ; Resistance
    db \<14> ; Weakness
    db \<15> ; StatusImmune
    db \<16> ; StatusPerm
    db \<17> ; MagiType or AI
    db \<18> ; Placeholder or RingCost
    db \<19> ; UNK
    dw $5EAE + $10*(ability\<20>) ; Ability1 - TODO Change $5EAE to label
    dw $5EAE + $10*(ability\<21>) ; Ability2
    dw $5EAE + $10*(ability\<22>) ; Ability3
    dw $5EAE + $10*(ability\<23>) ; Ability4
    db \<24> ; AbilityLevel1
    db \<25> ; AbilityLevel2
    db \<26> ; AbilityLevel3
    db \<27> ; AbilityLevel4
    AddressBank BITMAP_Fightscene_Creature_\<28>RLE ; Fightscene Tileset
    AddressBank ATTRTILE_Fightscene_Creature_\<28>RLE ; Fightscene Attrtilemap
    db \<29> ; AttrTilemap Width
    db \<30> ; AttrTilemap Height
    AddressBank PAL_Fightscene_Creature_\<31> ; Fightscene Palette
    AddressBank BITMAP_Cardscene_\<32> ; Cardscene Tileset
    db \<33> ; Cardscene Palette offset
ENDM